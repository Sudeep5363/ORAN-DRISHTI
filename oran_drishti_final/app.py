"""
app.py — Oran-Drishti Final: Streamlit Dashboard (Offline / Local Mode)
========================================================================
Loads synthetic GeoTIFF rasters generated by data_simulator.py and
runs the three-pronged Trishul AI pipeline:

  Prong 1 — Ritu-Chakra  : FFT harmonic comparison (healthy vs drift)
  Prong 2 — Shuddhi Model: LSTM autoencoder heatmap (Adharma scores)
  Prong 3 — Surya-Sakshi : Solar altitude confirmation

Prerequisites:
    python data_simulator.py   # generates data/ folder
    streamlit run app.py       # launches the dashboard
"""

import streamlit as st
import numpy as np
import matplotlib.pyplot as plt
import rasterio
from model_engine import extract_harmonics, build_lstm_autoencoder, calculate_sun_angle

# ---- Page configuration ----
st.set_page_config(layout="wide", page_title="Oran-Drishti")

st.title("🌿 Oran-Drishti: The Sacred Grove Sentinel")
st.markdown("### Aravalli Intelligence System: Unsupervised Land-Use Drift Detection")

st.markdown("---")

# ============================================================
# SECTION 1: Temporal Landscape Shift (Before / After Maps)
# ============================================================
st.subheader("1. Temporal Landscape Shift (Past vs. Present)")
st.caption("Standard Satellite Optical Data - Highlighted 'Silent Degradation'")

col1, col2 = st.columns(2)  # Two side-by-side columns for the Before/After maps

try:
    # Load the two synthetic GeoTIFFs produced by data_simulator.py.
    # rasterio.open() is used as a context manager to ensure the file
    # handle is closed after reading, even if an exception occurs.
    with rasterio.open('data/past_forest.tif') as src1:
        past_img = src1.read(1)      # Read band 1 into a 2-D NumPy array
    with rasterio.open('data/present_forest.tif') as src2:
        present_img = src2.read(1)

    # Render the Past (2023) raster using a Red-Yellow-Green colour map.
    # vmin=0, vmax=1 anchors the colour scale to the NDVI range.
    with col1:
        fig, ax = plt.subplots()
        ax.imshow(past_img, cmap='RdYlGn', vmin=0, vmax=1)
        ax.set_title("Purva-Sthiti (Past)\nAcquired: 2023-03-15")
        ax.axis('off')  # Hide axes ticks — we want a clean map view
        st.pyplot(fig)

    # Render the Present (2025) raster; the mining pit appears dark red.
    with col2:
        fig2, ax2 = plt.subplots()
        ax2.imshow(present_img, cmap='RdYlGn', vmin=0, vmax=1)
        ax2.set_title("Vartaman-Sthiti (Present)\nAcquired: 2025-05-15")
        ax2.axis('off')
        st.pyplot(fig2)

except Exception as e:
    # If the data files are missing, prompt the user to run the simulator first.
    st.error("⚠️ Data missing. Run 'python data_simulator.py' first.")

st.markdown("---")

# ============================================================
# SECTION 2: Trishul AI Analysis (triggered by button click)
# ============================================================
if st.button("🚀 Run Trishul AI (Harmonic & LSTM Analysis)", type="primary"):
    with st.spinner("Igniting the Digital Kshetrapal... Processing Time-Series..."):

        # --- Load the 3-D data cube from disk ---
        # Shape: (timesteps=36, height=50, width=50)
        cube = np.load('data/synthetic_aravalli_data.npy')
        timesteps, h, w = cube.shape

        # --- Extract representative time-series for two pixels ---
        # [0, 0] → corner pixel, assumed healthy (undisturbed baseline)
        # [25, 25] → centre pixel, inside the injected mining zone
        healthy_ts = cube[:, 0, 0]
        anomaly_ts = cube[:, 25, 25]

        # ============================================================
        # PRONG 1: Ritu-Chakra — FFT Harmonic Analysis
        # ============================================================
        st.subheader("⚙️ Prong 1: Ritu-Chakra (Fast Fourier Transform)")

        # Extract frequency-domain amplitudes for both pixels
        xf, healthy_amp = extract_harmonics(healthy_ts)
        _,  anomaly_amp = extract_harmonics(anomaly_ts)

        fig3, ax3 = plt.subplots(1, 2, figsize=(12, 3))

        # Left plot: time-domain comparison — healthy sine vs mining flatline
        ax3[0].plot(healthy_ts, color='green', label='Healthy (Sine Wave)')
        ax3[0].plot(anomaly_ts, color='red',   label='Drift (Flatline)')
        ax3[0].set_title("Vegetation Index Over 36 Months")
        ax3[0].legend()

        # Right plot: frequency-domain — healthy peak at 1/12 vs flat anomaly spectrum
        ax3[1].plot(xf, healthy_amp, color='green', label='Healthy Harmonic')
        ax3[1].plot(xf, anomaly_amp, color='red',   label='Broken Harmonic')
        ax3[1].set_title("Frequency Domain (Loss of Seasonality)")
        ax3[1].legend()

        st.pyplot(fig3)

        # ============================================================
        # PRONG 2: Shuddhi Model — LSTM Autoencoder Heatmap
        # ============================================================
        st.subheader("⚙️ Prong 2: Shuddhi Model (Unsupervised Deep Learning)")
        st.info("Training LSTM Autoencoder dynamically on healthy baseline...")

        # --- Prepare training data ---
        # Use only the top-left 5×5 corner of the grid as "healthy" training pixels.
        # Each pixel contributes one time-series of shape (timesteps, 1).
        X_train = cube[:, 0:5, 0:5].reshape(-1, timesteps, 1)  # shape: (25, 36, 1)

        # Build and train the LSTM autoencoder (unsupervised — no labels needed)
        model = build_lstm_autoencoder(timesteps, 1)
        model.fit(X_train, X_train, epochs=5, batch_size=4, verbose=0)
        # Note: verbose=0 suppresses per-epoch output to keep the Streamlit UI clean

        # --- Inference: reconstruct ALL pixels ---
        # Reshape the full cube to (h*w, timesteps, 1) for batch prediction
        flat_cube  = cube.reshape(-1, timesteps, 1)             # shape: (2500, 36, 1)
        predictions = model.predict(flat_cube, verbose=0)       # shape: (2500, 36, 1)

        # --- Compute Adharma Score (Mean Squared Error per pixel) ---
        # High MSE → the pixel's pattern was unusual relative to training data → anomaly
        mse        = np.mean(np.power(flat_cube - predictions, 2), axis=(1, 2))
        adharma_map = mse.reshape(h, w)  # Restore spatial shape: (50, 50)

        # Render the Adharma heatmap; 'hot' colourmap: black=normal, red/white=anomalous
        fig4, ax4 = plt.subplots(figsize=(8, 5))
        c = ax4.imshow(adharma_map, cmap='hot', interpolation='nearest')
        ax4.set_title("Adharma Alert Heatmap (High Reconstruction Error = Mining)")
        fig4.colorbar(c, ax=ax4, label="Reconstruction Loss")
        st.pyplot(fig4)

        # ============================================================
        # PRONG 3: Surya-Sakshi — Solar Physics Validator
        # ============================================================
        st.subheader("⚙️ Prong 3: Surya-Sakshi (Solar Physics Validator)")

